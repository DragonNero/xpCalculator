        <h4 class="text-primary text-xl text-center py-2 mb-2"> CO2 reduction and absorption </h4>
        <hr>
        <p class="prose mt-4">If you choose to travel responsibly or to participate in good cause projects by making a conscious use of your Miles, you will get rewarded with XP. Eligible activities include:<br>
        <ul>
            <li>&nbsp;&nbsp;&nbsp; Your Miles donations to charities that support causes such as: providing quality education, preserving sea life, reducing poverty, etc.</li>
            <li>&nbsp;&nbsp;&nbsp; Your participation in reforestation projects (possible with your Miles)</li>
            <li>&nbsp;&nbsp;&nbsp; Your purchase of Sustainable Aviation Fuel (SAF) to help Air France and KLM achieve a better energy mix, reduce their CO2 footprint and get closer to their decarbonation targets</li>
        </ul>
        <br>For every 2000 Miles spent, you get rewarded with 1XP.<br><br>
        You can also contribute with cash to reforestation projects or to the purchase of Sustainable Aviation Fuel and be rewarded with XP as well.<br>
